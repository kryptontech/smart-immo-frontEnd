package net.krypton.smartimmo.model;

public class AgenceModel {

	private String nomAgence;
	private String siteAgence;
	private String telFixe;
	private String addresse;
	private String logoAgence;
	
	public String getNomAgence() {
		return nomAgence;
	}
	public void setNomAgence(String nomAgence) {
		this.nomAgence = nomAgence;
	}
	public String getSiteAgence() {
		return siteAgence;
	}
	public void setSiteAgence(String siteAgence) {
		this.siteAgence = siteAgence;
	}
	public String getTelFixe() {
		return telFixe;
	}
	public void setTelFixe(String telFixe) {
		this.telFixe = telFixe;
	}
	public String getAddresse() {
		return addresse;
	}
	public void setAddresse(String addresse) {
		this.addresse = addresse;
	}
	public String getLogoAgence() {
		return logoAgence;
	}
	public void setLogoAgence(String logoAgence) {
		this.logoAgence = logoAgence;
	}
}
